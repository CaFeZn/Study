#include "pch.h"
#include <iostream>
#include <stdio.h>

int main()
{
	int A, B, C;
	scanf_s("%d%d%d",&A, &B, &C);
	if (A = B) {
		printf("C");
	}
	else if (A = C) {
		printf("B");
	}
	else if (C = B) {
		printf("A");
	}
	return 0;
}

